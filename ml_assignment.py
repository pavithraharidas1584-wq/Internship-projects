import math

dataset = [
    (150, 7, 'apple'),
    (130, 6, 'orange')
]

new_point = (140, 6.5)

def euclidean_distance(p1, p2):
    return math.sqrt((p1[0] - p2[0])**2 + (p1[1] - p2[1])**2)

nearest_label = None
min_distance = float('inf')

for fruit in dataset:
    distance = euclidean_distance(new_point, fruit[:2])

    print(f"Distance from {fruit[2]} = {distance}")

    if distance < min_distance:
        min_distance = distance
        nearest_label = fruit[2]

print("Predicted Label:", nearest_label)