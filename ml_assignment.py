"""**2. Find Nearest Point with Same X or Y**"""

def nearest_valid_point(x, y, points):
    min_distance = float('inf')
    index = -1

    for i, (px, py) in enumerate(points):
        if px == x or py == y:
            distance = abs(px - x) + abs(py - y)

            if distance < min_distance:
                min_distance = distance
                index = i

    return index

